    private void loadData(int seletedRow){
        Vector<Sach> listBook = new BookService().SelectAll();
        txtID.setText(listBook.get(seletedRow).getId() + "");
        txtID.setEditable(false);
        txtName.setText(listBook.get(seletedRow).getName());
        txtAmount.setText(listBook.get(seletedRow).getAmount()+"");
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        txtDate.setText(sdf.format(listBook.get(seletedRow).getPublicationDate()));
        txtPrice.setText(listBook.get(seletedRow).getPrice()+"");
        txtAuthor.setText(listBook.get(seletedRow).getAuthor());
        Vector<LoaiSach> listType = new TypeBookService().SelectAll();
        int count = 0;
        for(count = 0; count< listType.size(); count++){
            if(listType.get(count).getId() == listBook.get(seletedRow).getIdType()) break;
        }
        jcbType.setSelectedIndex(count);
    }
    
    private void loadJcb(){
        Vector<LoaiSach> listTypeBook = new TypeBookService().SelectAll();
        jcbType.removeAllItems();
        for (LoaiSach typeBook : listTypeBook) {
            jcbType.addItem(typeBook.toString());
        }
    }
    
