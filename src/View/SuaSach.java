    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Sach book = new Sach();
        if (txtName.getText().matches("[0-9a-zA-Z áàạảãâấậẩầẫăắằẳẵặđéẻèẽẹêểếềễệìỉíịĩòóỏõọôổốồỗộơởớờỡợùủúụũưửừứựữýỷỵỹỳÁẢẠÀÃẤẦẬẨẪÂĂẲẮẰẴẶĐÉẺÈẸẼÊỂẾỀỆỄÍỈÌỊĨÓÒỎỌÕÔỐỔỒỘỖƠỜỞỚỢỠÚỦÙỤŨƯỪỨỬỰỮÝỶỲỴỸ]+")) {
            if (txtPrice.getText().matches("[0-9]{1,}")) {
                if (txtAuthor.getText().matches("[0-9a-zA-Z áàạảãâấậẩầẫăắằẳẵặđéẻèẽẹêểếềễệìỉíịĩòóỏõọôổốồỗộơởớờỡợùủúụũưửừứựữýỷỵỹỳÁẢẠÀÃẤẦẬẨẪÂĂẲẮẰẴẶĐÉẺÈẸẼÊỂẾỀỆỄÍỈÌỊĨÓÒỎỌÕÔỐỔỒỘỖƠỜỞỚỢỠÚỦÙỤŨƯỪỨỬỰỮÝỶỲỴỸ]+")) {
                    if (txtDate.getText().matches("^[0-9]{1,2}/[0-9]{1,2}/[0-9]{4}$")) {
                        if (txtAmount.getText().matches("[0-9]{1,}")) {
                            try {
                                book.setId(Integer.parseInt(txtID.getText()));
                                book.setName(txtName.getText());
                                book.setPrice(Integer.parseInt(txtPrice.getText()));
                                book.setIdType(TypeBookService.nameToCode((String) jcbType.getSelectedItem()));
                                book.setAuthor(txtAuthor.getText());
                                book.setPublicationDate(new SimpleDateFormat("dd/MM/yyyy").parse(txtDate.getText()));
                                book.setAmount(Integer.parseInt(txtAmount.getText()));
                                
                                if(new BookService().Update(book)){
                                    JOptionPane.showMessageDialog(null, "Cập Nhật Thành Công !");
                                    this.dispose();
                                }
                                else{
                                    JOptionPane.showMessageDialog(null, "Cập Nhật Thất Bại !");
                                }

                            } catch (ParseException ex) {
                                Logger.getLogger(ThemSach.class.getName()).log(Level.SEVERE, null, ex);
                            }

                        } else {
                            JOptionPane.showMessageDialog(null, "Số lượng không khả dụng, vui lòng nhập lại ! ");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Ngày không khả dụng, vui lòng nhập lại ! ");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Tác Giả không đúng, vui lòng nhập lại ! ");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Giá Tiền không khả dụng, vui lòng nhập lại ! ");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Tên sách không khả dụng, vui lòng nhập lại ! ");
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

