    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if (jckAuthor.isSelected()) {
            Vector<Sach> listBook = new BookService().searchBy("author", txtSearch.getText());
            if (listBook.size()>0) {
                this.loadData(listBook);
            } else {
                JOptionPane.showMessageDialog(null, "Không Tìm Thấy !");
            }
        } else if (jckId.isSelected()) {
            Vector<Sach> listBook = new BookService().searchBy("id", txtSearch.getText());
            if (listBook.size()>0) {
                this.loadData(listBook);
            } else {
                JOptionPane.showMessageDialog(null, "Không Tìm Thấy !");
            }
        } else if (jckName.isSelected()) {
            Vector<Sach> listBook = new BookService().searchBy("name", txtSearch.getText());
            if(listBook.size()>0){
                this.loadData(listBook);
            }
            else{
                JOptionPane.showMessageDialog(null, "Không Tìm Thấy !");
            }
        } else if (jckType.isSelected()) {
            Vector<Sach> listBook = new BookService().searchByIdType(new TypeBookService().search(txtSearch.getText()));
            if(listBook.size()>0){
                this.loadData(listBook);
            }
            else{
                JOptionPane.showMessageDialog(null, "Không Có Mục Nào !");
            }
        } else if (jckAll.isSelected()) {
            Vector<Sach> listBook = new BookService().searchByAll(txtSearch.getText());
            if(listBook.size()>0){
                this.loadData(listBook);
            }
            else{
                JOptionPane.showMessageDialog(null, "Không Có Mục Nào !");
            }
        }

    }//GEN-LAST:event_btnSearchActionPerformed

