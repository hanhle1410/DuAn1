    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (tableListBook.getSelectedRow() > -1) {
            int x = JOptionPane.showConfirmDialog(null, "Bạn Muốn Xóa?", "", JOptionPane.YES_NO_OPTION);
            if (x == 0) {
                BookService bs = new BookService();
                Vector<Sach> listBook = new BookService().SelectAll();

                if (bs.Delete(listBook.get(tableListBook.getSelectedRow()).getId())) {
                    JOptionPane.showMessageDialog(null, "Đã Xóa !");
                    this.loadData(new BookService().SelectAll());
                } else {
                    JOptionPane.showMessageDialog(null, "Xóa Thất Bại !");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Không Có Mục Nào Được Chọn !");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

